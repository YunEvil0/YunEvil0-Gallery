package com.traveler54.gallery.exception;

public class BisException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6215418659680684324L;

	public BisException() {
		super();
	}

	public BisException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public BisException(String msg) {
		super(msg);
	}

	public BisException(Throwable cause) {
		super(cause);
	}
}
